    private void jbCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCalcularActionPerformed
        // TODO add your handling code here:
        
        double capital = Double.parseDouble(jtfCapital.getText());
        double taxa = Double.parseDouble(jtfTaxa.getText())/100;
        double montante = Double.parseDouble(jtfMontante.getText());
        
        double cacl1 = montante / capital;
        double calc2 = Math.log(cacl1)/Math.log(1+taxa);
        
        
        jlResultado.setText("Tempo: "+calc2+" meses");
    }//GEN-LAST:event_jbCalcularActionPerformed

